func TestPart1(t *testing.T) {
	testdata := []struct {
		name  string
		input []string
		want  int
	}{
		{
			name:  "example",
			input: example,
			want:  13140,
		},
		{
			name:  "input",
			input: input,
			want:  12880,
		},
	}

	for _, tt := range testdata {
		t.Run(tt.name, func(t *testing.T) {
			got, err := part1(tt.input)
			if err != nil {
				t.Error(err)
			}

			if got != tt.want {
				t.Errorf("Want part1(tt.input)=%d; got %d", tt.want, got)
			}
		})
	}
}

func TestPart2(t *testing.T) {
	testdata := []struct {
		name  string
		input []string
		want  []string
	}{
		{
			name:  "example",
			input: example,
			want:  exampleScreen,
		},
		{
			name:  "input",
			input: input,
			want:  inputScreen,
		},
	}

	for _, tt := range testdata {
		t.Run(tt.name, func(t *testing.T) {
			got, err := part2(tt.input)
			if err != nil {
				t.Error(err)
			}

			if !reflect.DeepEqual(got, tt.want) {
				t.Errorf("Want part2(tt.input)=\n%s\n; got \n%s\n", strings.Join(tt.want, "\n"), strings.Join(got, "\n"))
			}
		})
	}
}
