func TestPart1(t *testing.T) {
	testdata := []struct {
		name  string
		input []string
		want  int
	}{
		{
			name:  "example",
			input: example,
			want:  10092,
		},
		{
			name:  "input",
			input: input,
			want:  1463512,
		},
	}

	for _, tt := range testdata {
		t.Run(tt.name, func(t *testing.T) {
			got, err := part1(tt.input)
			if err != nil {
				t.Error(err)
				return
			}

			if got != tt.want {
				t.Errorf("Want part1(tt.input)=%d; got %d", tt.want, got)
			}
		})
	}
}

func TestMoves(t *testing.T) {
	m := charmap.Parse([]string{
		"@[][].",
	})
	maybeMove(geom.E, m)

	if got := m.AsString('_'); got != ".@[][]" {
		t.Errorf(`want ".@[][]"; got %q`, got)
	}
}

